export type USStateCode = keyof typeof US_STATES

export const US_STATE_OPTIONS = Object.entries(US_STATES).map(([code, name]) => ({
    value: code,
    label: `${code} - ${name}`,
}))

// Helper to get full state name from code
export function getStateName(code: USStateCode): string {
    return US_STATES[code]
}

// Validate if a string is a valid US state code
export function isValidUSState(code: string): code is USStateCode {
    return code in US_STATES
}
